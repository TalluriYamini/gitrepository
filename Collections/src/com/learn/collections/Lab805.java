package com.learn.collections;

import java.util.LinkedHashSet;

class Lab805 {

	public static void main(String[] args) {
		LinkedHashSet set=new LinkedHashSet();
		set.add("Sri");
		set.add("Nivas");
		set.add("99");
		set.add("DK");
		set.add("Manish");
		System.out.println(set);
	}
}

