package com.learn.collections;

import java.util.HashSet;

class Lab804 {

	public static void main(String[] args) {
		HashSet set=new HashSet();
		set.add("Sri");
		set.add("Nivas");
		set.add("99");
		set.add("DK");
		set.add("Manish");
		System.out.println(set);
	}
}

// output

/*
	
				[99, Nivas, DK, Sri, Manish]
	
 */
